// >= 80 Lulus - Predikat A 🌟
// >= 60 Lulus - Predikat B 👍
// >= 40 Lulus - Predikat C 😅
// < 40 Tidak Lulus ❌

const nilai = 20 

if(nilai >= 80) {
    console.log("Lulus - Predikat A 🌟")
} else if(nilai >= 60) {
    console.log("Lulus - Predikat B 👍")
} else if(nilai >= 40) {
    console.log("Lulus - Predikat C 😅")
} else {
    console.log("Tidak Lulus ❌")
}
